# 51_for_range_variations.py

n = 5

print('range(0, n)')
print('-----------')
for i in range(0, n):
    print(i)

print('range(n)')
print('-----------')
for i in range(n):
    print(i)

print('(0, 1, 2, 3, 4)')
print('-----------')
for i in (0, 1, 2, 3, 4):
    print(i)

print('my_list')
my_list = (0, 1, 2, 3, 4)
for i in my_list:
    print(i)
