# 85_lines_and_lines.py


def printLine(length):
    for i in range(length):
        print("_", end="")
    print()


def main():
    printLine(1)
    printLine(2)
    printLine(3)
    printLine(4)
    printLine(5)
    printLine(6)
    printLine(7)
    printLine(8)
    printLine(9)
    printLine(10)


main()
